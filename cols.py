# ANSI color codes for text
black = '\033[30m'
red = '\033[31m'
green = '\033[32m'
yellow = '\033[33m'
blue = '\033[34m'
magenta = '\033[35m'
cyan = '\033[36m'
white = '\033[37m'
reset = '\033[0m'  # Reset text color to default

# ANSI color codes for background
bg_black = '\033[40m'
bg_red = '\033[41m'
bg_green = '\033[42m'
bg_yellow = '\033[43m'
bg_blue = '\033[44m'
bg_magenta = '\033[45m'
bg_cyan = '\033[46m'
bg_white = '\033[47m'
bg_reset = '\033[49m'  # Reset background color to default

# ANSI escape codes for text formatting
reset = '\033[0m'  # Reset text formatting to default
bold = '\033[1m'  # Bold text
italic = '\033[3m'  # Italic text
underline = '\033[4m'  # Underline text